package EJERCICIOS.EjercicioSuma;

public class FuncionSUma {
    public static void main(String[] args) {
        System.out.println("\nSuma de Valores");
        System.out.println(funcionSuma(2, 3, 4));
    }

    private static int funcionSuma(int i, int j, int k) {
        return i + j + k;
    }
}
